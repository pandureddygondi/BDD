package stevejobs;
public class Auto96 
{
	public static void main(String[] args) 
	{
		for(int i=0;i<=5;i++)
		{
			for(int j=5;j>i;j--)
			{
				System.out.print(" ");
			}
			for(int k=1;k<=i;k++)
			{
				System.out.print("*");
			}
			for(int r=1;r<i; r++)
			{
				System.out.print("*");
			}
			System.out.println();
	}

 }
}
